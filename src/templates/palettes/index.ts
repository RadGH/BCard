export function getPalette(id: string): PaletteEntry | undefined {
  return palettes.find(p => p.id === id);
}

export function getAllPalettes(): PaletteEntry[] {
  return palettes;
}

export function getDefaultPaletteForLayout(layoutId: string): PaletteEntry {
  const match = palettes.find(p => p.defaultFor.includes(layoutId));
  return match ?? palettes[0];
}
